class Solution {
  public:
	int subtractProductAndSum(int n) {
		int nums = n;
		int product = 1;
		int sum = 0;
		while (nums) {
			int left = (nums % 10);
			product *= left;
			sum += left;
			nums = nums / 10 ;
		}
		return product - sum;
	}

  private:
};

