class Solution {
  public:
	int numIdenticalPairs(vector<int>& nums) {
		vector<int> dict(101, 0);
		int ans = 0;
		for (int i = 0; i < nums.size(); ++i) {
			if (!dict[nums[i]]) dict[nums[i]]++;
			else {
				ans += dict[nums[i]]++;
			}
		}
		return ans;
	}

  private:
};

